package com.kosta.vo;

public class L_CultureVO {
	
	private int num, anum, state;
	private String id, culture_date;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getAnum() {
		return anum;
	}
	public void setAnum(int anum) {
		this.anum = anum;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCulture_date() {
		return culture_date;
	}
	public void setCulture_date(String culture_date) {
		this.culture_date = culture_date;
	}
}
